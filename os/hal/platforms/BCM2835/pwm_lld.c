/**
 * @brief   Low level PWM driver initialization.
 *
 * @notapi
 */
void pwm_lld_init(void) {
  pwmObjectInit(&PWMD1);
}

/**
 * @brief   Configures and activates the PWM peripheral.
 *
 * @param[in] pwmp      pointer to the @p PWMDriver object
 *
 * @notapi
 */
void pwm_lld_start(PWMDriver *pwmp) {
  /* Set PWM pin function.*/
  bcm2835_gpio_fnsel(18, GPFN_ALT5);

  /* Stop PWM.*/
  PWM_CTL = 0 ;	

  /* Disable clock generator (reset bit 4).*/
  GPIO0_CLK_CTL = GPIO_CLK_PWD | 0x01 ;	
  delayMicroseconds (110) ;

  /* Wait for clock to be !BUSY.*/
  while ((GPIO0_CLK_CTL & 0x80) != 0);

  /* set pwm div to 32 (19.2/32 = 600KHz).*/
  GPIO0_CLK_DIV = GPIO_CLK_PWD | (32 << 12);

  /* enable clock generator.*/
  GPIO0_CLK_CTL = GPIO_CLK_PWD | 0x11;

  /* N/M -- N = DATA, M = RANGE.*/
  /* M/S -- M = DATA, S = RANGE.*/
  PWM0_DATA = 0; 
  PWM0_RANGE = pwmp->period; 

}

/**
 * @brief   Deactivates the PWM peripheral.
 *
 * @param[in] pwmp      pointer to the @p PWMDriver object
 *
 * @notapi
 */
void pwm_lld_stop(PWMDriver *pwmp) {
  UNUSED(pwmp);
  PWM_CTL &= ~PWM0_ENABLE;
}

/**
 * @brief   Changes the period the PWM peripheral.
 * @details This function changes the period of a PWM unit that has already
 *          been activated using @p pwmStart().
 * @pre     The PWM unit must have been activated using @p pwmStart().
 * @post    The PWM unit period is changed to the new value.
 * @note    The function has effect at the next cycle start.
 * @note    If a period is specified that is shorter than the pulse width
 *          programmed in one of the channels then the behavior is not
 *          guaranteed.
 *
 * @param[in] pwmp      pointer to a @p PWMDriver object
 * @param[in] period    new cycle time in ticks
 *
 * @notapi
 */
void pwm_lld_change_period(PWMDriver *pwmp, pwmcnt_t period) {
  UNUSED(pwmp);
  PWM0_RANGE = period;
}

/**
 * @brief   Enables a PWM channel.
 * @pre     The PWM unit must have been activated using @p pwmStart().
 * @post    The channel is active using the specified configuration.
 * @note    Depending on the hardware implementation this function has
 *          effect starting on the next cycle (recommended implementation)
 *          or immediately (fallback implementation).
 *
 * @param[in] pwmp      pointer to a @p PWMDriver object
 * @param[in] channel   PWM channel identifier (0...PWM_CHANNELS-1)
 * @param[in] width     PWM pulse width as clock pulses number
 *
 * @notapi
 */
void pwm_lld_enable_channel(PWMDriver *pwmp,
                            pwmchannel_t channel,
                            pwmcnt_t width) {
  UNUSED(pwmp);
  UNUSED(channel);

  PWM_CTL |= PWM0_ENABLE;
  PWM0_DATA = width;
}

/**
 * @brief   Disables a PWM channel.
 * @pre     The PWM unit must have been activated using @p pwmStart().
 * @post    The channel is disabled and its output line returned to the
 *          idle state.
 * @note    Depending on the hardware implementation this function has
 *          effect starting on the next cycle (recommended implementation)
 *          or immediately (fallback implementation).
 *
 * @param[in] pwmp      pointer to a @p PWMDriver object
 * @param[in] channel   PWM channel identifier (0...PWM_CHANNELS-1)
 *
 * @notapi
 */
void pwm_lld_disable_channel(PWMDriver *pwmp, pwmchannel_t channel) {
  UNUSED(pwmp);
  UNUSED(channel);
  PWM_CTL &= ~PWM0_ENABLE;
}

